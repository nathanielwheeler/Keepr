<template>
	<div class="dashboard">
		<h1>Welcome Home {{user.username}}</h1>
		<button v-if="user.id" @click="logout">logout</button>
		<router-link v-else :to="{name: 'login'}">Login</router-link>
	</div>
</template>

<script>
export default {
	name: "dashboard",
	computed: {
		user() {
			return this.$store.state.user;
		}
	},
	methods: {
		logout() {
			this.$store.dispatch("logout");
		}
	}
};
</script>